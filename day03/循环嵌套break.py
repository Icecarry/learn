'''
* 外循环打印 1 到 9 数字
    * 内循环打印 1 到 9 数字
    * 当内循环遍历到 4 的时候，使用 break 打算内循环
    * 观察外循环是否正常打印结束
'''

#外循环打印1 - 9
i = 1

while i < 10:
    #内循环打印1 - 9
    j = 1
    while j < 10:
        if j == 4:
            print("内循环等于4")
            break
        print("内循环：%d" % j)
        j += 1
    print("外循环 %d" % i)
    i += 1